package com.example.canteenapp.Registration;

public class Canteen
{
    public String Canteenemail;
    public String Canteenname;
    public String Canteenphone;
    public String Canteenpassword;
    public Canteen(String Canteenemail, String Canteenname, String Canteenphone, String Canteenpassword) {
        this.Canteenemail = Canteenemail;
        this.Canteenname = Canteenname;
        this.Canteenphone = Canteenphone;
        this.Canteenpassword = Canteenpassword;
    }

}
